#Faça um programa que receba dois códigos de rastreio e informe se eles são iguais ou diferentes.

#Variáveis
R1 = input("Digite o primeiro código de rastreio: ")
R2 = input("Digite o segundo código de rastreio: ")

if R1 == R2:
    print(f"{R1} e {R2} são iguais !!!")
else:
    print(f"{R1} e {R2} são diferentes !!!")

    